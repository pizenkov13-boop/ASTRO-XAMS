function toFillQuestion(item: OgeItem): Question {
  const correct = Array.isArray(item.correct) ? item.correct : [item.correct];
  return {
    id: item.id,
    type: "fill_blank",
    prompt: item.prompt,
    correctAnswer: correct,
    explanation: item.explain,
    timeLimitSec: 90,
  };
}

