function buildUnit(
  id: string,
  number: number,
  title: string,
  description: string,
  items: OgeItem[]
): Unit {
  return {
    id,
    moduleId: "oge",
    number,
    title,
    level: "ОГЭ",
    description,
    questions: items.map(toFillQuestion),
  };
}

