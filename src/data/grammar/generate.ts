export function generateGrammarUnits(): Unit[] {
  const count = Math.min(UNIT_TITLES.length, 115);
  const units: Unit[] = [];

  for (let i = 0; i < count; i++) {
    const n = i + 1;
    const meta = UNIT_TITLES[i];
    units.push({
      id: `grammar-unit-${n}`,
      moduleId: "grammar",
      number: n,
      title: `Unit ${n}: ${meta.title}`,
      level: meta.level,
      description: `Practice: ${meta.topic}`,
      questions: getUnitQuestions(n, meta.topic, meta.title),
    });
  }

  return units;
}
